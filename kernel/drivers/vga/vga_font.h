// Draw character with shadow for depth effect
static inline void vga_draw_char_with_shadow(uint32_t x, uint32_t y, char c, 
                                             uint8_t color, uint8_t shadow_color,
                                             void (*set_pixel)(uint32_t, uint32_t, uint8_t)) {
    unsigned char uc = (unsigned char)c;
    if (uc >= 128) return;
    
    // Draw shadow first (offset by 1 pixel)
    for (uint32_t row = 0; row < 8; row++) {
        uint8_t bits = font_8x8[uc][row];
        for (uint32_t col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                set_pixel(x + col + 1, y + row + 1, shadow_color);
            }
        }
    }
    
    // Draw main character
    for (uint32_t row = 0; row < 8; row++) {
        uint8_t bits = font_8x8[uc][row];
        for (uint32_t col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                set_pixel(x + col, y + row, color);
            }
        }
    }
}

// Draw string with shadow
static inline void vga_draw_string_with_shadow(uint32_t x, uint32_t y, const char* str,
                                               uint8_t color, uint8_t shadow_color,
                                               void (*set_pixel)(uint32_t, uint32_t, uint8_t)) {
    uint32_t offset = 0;
    while (*str) {
        vga_draw_char_with_shadow(x + offset, y, *str, color, shadow_color, set_pixel);
        str++;
        offset += 8;
    }
}

